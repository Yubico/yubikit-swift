    @Test("COSE.Key with test vector (EC2 P-256)")
    func testCOSEKeyEC2P256TestVector() throws {
        // P-256 public key coordinates in hex
        let x = Data([
            0xba, 0xc5, 0xb1, 0x1c, 0xad, 0x8f, 0x99, 0xf9, 0xc7, 0x2b, 0x05, 0xcf,
            0x4b, 0x9e, 0x26, 0xd2, 0x44, 0xdc, 0x18, 0x9f, 0x74, 0x52, 0x28, 0x25,
            0x5a, 0x21, 0x9a, 0x86, 0xd6, 0xa0, 0x9e, 0xff,
        ])
        let y = Data([
            0x20, 0x13, 0x8b, 0xf8, 0x2d, 0xc1, 0xb6, 0xd5, 0x62, 0xbe, 0x0f, 0xa5,
            0x4a, 0xb7, 0x80, 0x4a, 0x3a, 0x64, 0xb6, 0xd7, 0x2c, 0xcf, 0xed, 0x6b,
            0x6f, 0xb6, 0xed, 0x28, 0xbb, 0xfc, 0x11, 0x7e,
        ])

        let key = COSE.Key.ec2(alg: .es256, kid: nil, crv: 1, x: x, y: y)

        // Verify coordinate sizes match P-256
        #expect(x.count == 32)
        #expect(y.count == 32)

        // Verify round-trip encoding
        let encoded = key.cbor()
        let decoded = try #require(COSE.Key(cbor: encoded))
        #expect(decoded == key)
    }

