    @Test func decompressGzipCertificate() throws {
        #expect(Self.gzipCompressedCertificate.isGzipped == true)

        // Expected length from gzip footer: 0x012f = 303 bytes
        let decompressed = try Self.gzipCompressedCertificate.decompressCertificate()
        #expect(decompressed == Self.testCertificateDER)

        let cert = X509Cert(der: decompressed)
        #expect(cert.der.count == 303)
    }

    @Test func decompressNetIdCertificate() throws {
        #expect(Self.netIdCompressedCertificate.isNetId == true)

        let decompressed = try Self.netIdCompressedCertificate.decompressCertificate()
        #expect(decompressed == Self.testCertificateDER)
    }

    @Test func decompressUnsupportedFormat() throws {
        // Random data that doesn't start with Net iD (0x01 0x00) or gzip (0x1f 0x8b) magic
        let unknownData = Data([0x30, 0x82, 0x01, 0x2b, 0x30, 0x81, 0xd2, 0xa0])

        #expect(unknownData.isNetId == false)
        #expect(unknownData.isGzipped == false)

        #expect(throws: CompressionError.unsupportedFormat) {
            try unknownData.decompressCertificate()
        }
    }
